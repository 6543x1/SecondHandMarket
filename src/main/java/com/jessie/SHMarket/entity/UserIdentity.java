package com.jessie.SHMarket.entity;


public class UserIdentity
{

  private int uid;
  private String no;
  private String school;


  public int getUid()
  {
    return uid;
  }

  public void setUid(int uid)
  {
    this.uid = uid;
  }


  public String getNo()
  {
    return no;
  }

  public void setNo(String no)
  {
    this.no = no;
  }


  public String getSchool()
  {
    return school;
  }

  public void setSchool(String school)
  {
    this.school = school;
  }

}
